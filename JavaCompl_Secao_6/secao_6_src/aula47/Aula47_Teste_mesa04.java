public class Aula47_Teste_mesa04 {

	public static void main(String[] args) {

		System.out.println("---------------------------------------------------------------");
		System.out.println("---> Seção-6 Aula 47 - Exercícios de Teste de mesa 04 com while ");	
		
		int x = 0;
		
		while (x < 5) {
			int y = x * 3;
			System.out.print(y);
			x = x + 1;
			System.out.println( x + " - " + y);
		}
		System.out.println("Fim");
		
		
		System.out.printf(" Valor Final: X = %d %n ", x);

		System.out.println("        ---> FIM <--- ");
		System.out.println("---------------------------------------------------------------");

	}

}

