public class Aula50_For_02Contagem {

	public static void main(String[] args) {

		System.out.println(
				"-------------------------------------------------------------------------------------------------");
		System.out.println("-----> Seção-6 Aula 50 - For  CONTAGEM Regressiva ");
		System.out.println("---------------------------------------------------------");
		System.out.println(" --- Importante --- ");
		System.out.println(
				"Perceba que a estrutura \"para\" é ótima para se fazer uma repetição baseada em uma CONTAGEM:");
		System.out.println("  ");
		System.out.println("	-------	Código em Java	-------	");

		System.out.println(" Inicio de Contagem:");

		for (int i = 4; i >= 0; i--) {
			System.out.println("Valor de i: " + i);
		}

		System.out.println("           ---> FIM <--- ");
		System.out.println(
				"-------------------------------------------------------------------------------------------------");

	}

}


